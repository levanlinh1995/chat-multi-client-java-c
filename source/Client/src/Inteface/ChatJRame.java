    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        String msg = jTextField1.getText();
        String target = jList1.getSelectedValue().toString();

        if(!msg.isEmpty() && !target.isEmpty() && target !="All"){
            jTextField1.setText("");
            
            try {
                client.SendPrivateMsg(client.os, msg , target);
            } catch (IOException ex) {
                Logger.getLogger(ChatJRame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        // Nếu tn dc soạn và không chọn target hết.
        if(!msg.isEmpty() && !target.isEmpty() && target =="All")
        {
            jTextField1.setText("");
            try {
                client.SendPublicMsg(client.os,msg);
            } catch (IOException ex) {
                Logger.getLogger(ChatJRame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

