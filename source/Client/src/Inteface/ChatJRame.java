    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();        
        int returnValue = fc.showOpenDialog(this); 
        String target = jList1.getSelectedValue().toString();
        
        if(returnValue == JFileChooser.APPROVE_OPTION)
        {
            File f = fc.getSelectedFile();
            try {
                byte[] data = Files.readAllBytes(f.toPath());
                client.FileShare = data;
                client.FileName = f.getName();
                //Send.
                jTextField2.setText(f.toPath().toString());
            } catch (IOException ex) {
                Logger.getLogger(ChatJRame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if(f.toPath().toString() != null && !target.isEmpty() && target !="All" )
            {
                jTextField1.setText("");
                try {
                    client.setupSendFile(client.os , target);
                    JOptionPane.showMessageDialog(this, "Trans file complete.");
                } catch (IOException ex) {
                    Logger.getLogger(ChatJRame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }      
    }//GEN-LAST:event_jButton2MouseClicked

